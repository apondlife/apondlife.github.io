    <h3>long description</h3>
    <p>
      discone is an open-world move-em-up: it centers the expressiveness and feeling of moving a character in 3d space. it seeks
      to design an experience where that is enough: where movement is meaningful enough that it's a sufficient invitation for
      players to play. it's an inscrutable world full of characters to talk to, who both say cute things to you and who you then
      embody – jumping into their body directly. every character walks, runs, jumps, and feels dinsticlty, and each provides a
      player the opportunity to explore and discover the nuances of that character's individual expression, their individual
      being. there are no player or non-player characters in discone. there are simply characters.
    </p>

    <p>
      similarly, discone's open, partially-generated world provides a canvas for players to explore the interactions of different
      characters with different surfaces, with their capacity to touch. it's a playground or toybox. it describes no explicit
      objectives or quests, but instead surfaces a litany of obstacles. why and how and what obstacles players choose to tackle is
      up to them, their goals are theirs to discover, not ours to prescribe.
    </p>

    <p>
      and they don't need to do it alone. discone is an online, mingleplayer videogame. players can see one another, express
      themselves and communicate using their bodies & learned movements, and explore the world together. there are no collisions,
      no physics – what is networked is kept deliberately simple – but it provides a space for friends and strangers to exist,
      play, and hang out.
    </p>

    <p>
      we owe a debt to those who came before us. bernie dekoven, for the push to play well and to build community – we strive to
      play as well as he did. also "[fr0g] clan official server 24/7 zk map (for stranger)", "red desert render", bennett foddy,
      "juice galaxy", olia lialina, "final fantasy xi", &c.
    </p>

    <h3>Artists Statement</h3>
    <div>
      <p>there are no goals in this videogame, but we have some goals.</p>

      <p>
        discone is an exploration of videogames beyond gameness: beyond designed rules and designer-defined objectives. we, as
        designers, create the physics, the space, and the characters. players create the play.
      </p>

      <p>
        our goal is to create an exciting world that allows players to move around in their expressive character-bodies. to
        explore and pull at the videogame's seams, left there intentionally or unintentionally. we believe that a novel,
        intuitively explorable, & experientially understood movement system situated in an online space has infinite potential,
        and we are excited to explore this possibility space.
      </p>

      <p>
        discone affords the open exploration and discovery—the ability to pick a point on the horizon—of "elden ring" but without
        any combat or external conflict. it affords the casual socialization and community of games like "runescape" or "club
        penguin" but provides it in the context of a challenge-oriented, self-directed 3d platformer. like "getting over it", it
        affords the exciting stress of embodying and manipulating an alien avatar along a path but provides it via an infinity of
        player-selected paths. are those mountains even climbable? (they definitely are)
      </p>

      <p>
        we're crafting a visual style that evinces a no-frills approach to artmaking. it embraces simplicity and childlike wonder,
        leans into the constraints of exploring limited tools like mspaint & kidpix (or the pixel), and explores the territory
        outside the boundaries of traditional art & its received aesthetic norms. we are inspired by paint and exploring
        pixel-based art (all screen art is pixel art) as an impressionism, as something beyond chunky, low-resolution pixels. our
        approach to 3d-rendering accepts & embraces the glitchiness, the inherent flatness of polygons & deformed meshes, its
        seams & pointed edges.
      </p>

      <p>discone is a game with no bosses and no ladders.</p>
